let isRunning = false;
let currentIndex = 0;
let loadIntervalId = null;
let openTabs = [];

chrome.runtime.onMessage.addListener((request, sender, sendResponse) => {
  if (request.action === 'start') {
    startLoading();
    sendResponse({success: true});
  } else if (request.action === 'stop') {
    stopLoading();
    sendResponse({success: true});
  }
});

async function startLoading() {
  if (isRunning) return;
  
  isRunning = true;
  currentIndex = 0;
  openTabs = [];
  
  await chrome.storage.local.set({isRunning: true, currentIndex: 0});
  
  updateStatus(`Starting... (Site 1/${TOP_1000_WEBSITES.length})`);
  
  loadIntervalId = setInterval(async () => {
    if (currentIndex >= TOP_1000_WEBSITES.length) {
      stopLoading();
      return;
    }
    
    await loadNextWebsite();
    currentIndex++;
    
    await chrome.storage.local.set({currentIndex: currentIndex});
  }, 5000);
  
  // Load first website immediately
  await loadNextWebsite();
  currentIndex++;
}

async function loadNextWebsite() {
  if (currentIndex >= TOP_1000_WEBSITES.length) return;
  
  const url = TOP_1000_WEBSITES[currentIndex];
  updateStatus(`Loading ${url} (${currentIndex + 1}/${TOP_1000_WEBSITES.length})`);
  
  try {
    // Create new tab
    const newTab = await chrome.tabs.create({
      url: url,
      active: false
    });
    
    openTabs.push(newTab.id);
    
    // Keep maximum of 50 tabs open, close the oldest ones
    if (openTabs.length > 50) {
      const tabToClose = openTabs.shift();
      try {
        await chrome.tabs.remove(tabToClose);
      } catch (error) {
        console.log('Tab already closed:', tabToClose);
      }
    }
    
  } catch (error) {
    console.error('Error loading website:', url, error);
    updateStatus(`Error loading ${url}: ${error.message}`);
  }
}

async function stopLoading() {
  isRunning = false;
  
  if (loadIntervalId) {
    clearInterval(loadIntervalId);
    loadIntervalId = null;
  }
  
  await chrome.storage.local.set({isRunning: false});
  
  // Close all opened tabs
  for (const tabId of openTabs) {
    try {
      await chrome.tabs.remove(tabId);
    } catch (error) {
      console.log('Tab already closed:', tabId);
    }
  }
  
  openTabs = [];
  updateStatus('Stopped');
}

function updateStatus(status) {
  // Try to send status to popup if it's open
  chrome.runtime.sendMessage({
    action: 'statusUpdate',
    status: status
  }).catch(() => {
    // Popup not open, ignore error
  });
}